//	Sqrt
//----------------------------------------------------------------------------------------------
LONGLONG Sqrt( LONGLONG X )
{
	//	ïœêîêÈåæ
	LONGLONG	S	= 1;
	LONGLONG	T	= X;

	if( X <= 0 )
	{
		return( 0 );
	}

	while( S < T )
	{
		S	<<= 1;
		T	>>= 1;
	}

	do
	{
		T	= S;
		S	= ( X / S + S ) >> 1;
	} while( S < T );

	return( T );
}
